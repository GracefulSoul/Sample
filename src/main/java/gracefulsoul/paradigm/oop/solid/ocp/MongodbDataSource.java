package gracefulsoul.paradigm.oop.solid.ocp;

public class MongodbDataSource extends DataSource {

	private String host;
	private int port;

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}

}
